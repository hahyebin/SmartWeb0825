package ex8_Object;

public class Member {
	
	private String id;
	private String pwd;
	
	public Member(String id) {
		super();
		this.id = id;
	
	}
	

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPwd() {
		return pwd;
	}

	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	
	

}
